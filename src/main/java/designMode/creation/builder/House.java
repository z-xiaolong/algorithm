public class House {

    private String basie;
    private String wall;
    private String roof;

    public String getBasie() {
        return basie;
    }

    public void setBasie(String basie) {
        this.basie = basie;
    }

    public String getWall() {
        return wall;
    }

    public void setWall(String wall) {
        this.wall = wall;
    }

    public String getRoof() {
        return roof;
    }

    public void setRoof(String roof) {
        this.roof = roof;
    }
}
